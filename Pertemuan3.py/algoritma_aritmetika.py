x = 5
y = 6

#add
total = x+y
print(f"total add from {x} + {y} = {total}")

#kurang
total = x*y
print(f"total kurang from {x} * {y} = {total}")

#bagi
total = x/y
print(f"total bagi from {x} / {y} = {total}")
total = int(total)
print(f"total bagi conversi from {x} / {y} = {total}")
total = x // y
print(f"total bagi singkat from {x} // {y} = {total}")

# modulus
total = x % y
print(f"total modulus from {x} % {y} = {total}")

